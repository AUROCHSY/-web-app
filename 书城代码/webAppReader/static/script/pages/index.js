//连通界面与数据接口
$.get('/ajax/index',function(d){//接口的的数据返回格式可以通过打debugger在develop tool上查看 
	//debugger; 
	new Vue({
		el:'#app',//绑定作用域:id为app的元素内(包含子元素) 
		data:{/*首页服务端数据绑定*/
			//test:"测试数据",将右边的值赋值给左边的变量 
			top:d.items[0].data.data,//顶部轮播图
			hot:d.items[1].data.data,//热门书籍数据
			recommend:d.items[2].data.data,//推荐书籍数据
			female:d.items[3].data.data,
			male:d.items[4].data.data,
			free:d.items[5].data.data,
			topic:d.items[6].data.data,//专题
			duration:0,//商城书架动画切换持续时间
			position:0,//主体页位置控制
			header_position:0,//顶栏选中光标头部位置控制
			header_duration:0,
			tab_1_class:'Swipe-tab__on',//用于动态控制两个标签样式的类
			tab_2_class:''
		},
		methods:{
			tabSwitch:function(pos){
				//debugger;
				this.duration=0.5;
				this.header_duration=0.5;
				if(pos==0){
					this.position=0;
					this.header_position=0;
					this.tab_1_class="Swipe-tab__on";
					this.tab_2_class="";
				}
				else{
					this.position=(-734);
					this.header_position=277;
					this.tab_1_class="";
					this.tab_2_class="Swipe-tab__on";
				}
			}
		}
	})
},'json')//指定接口的数据返回格式 